    public static boolean validarFormatoPrecio(String precio) {
        //validar si es nulo
        if (precio == null || precio.isEmpty()) {
            return false;
        }
        
        //Validar si empieza con . para ponerle 0 al principio
        if (precio.startsWith(".")) {
            precio = "0" + precio;
        }
        
        //divide el texto para verificar 
        String[] partes = precio.split("\\.");

        //si es mas de dos puntos falsoo
        if (partes.length > 2) {
            return false;
        }
        
        //Validaciones con formato
        String parteEntera = partes[0];
        if (!parteEntera.matches("\\d+") || parteEntera.length() > 10) {
            return false;
        }
        
        //Validaciones minuciosas
        if (partes.length == 2) {
            String parteDecimal = partes[1];
            if (!parteDecimal.matches("\\d+")) {
                return false;
            }
            if (parteDecimal.length() > 1) {
                return false;
            }
        } else if (partes.length == 1 && !parteEntera.matches("\\d+")) {
            return false;
        }

        try {
            Double.valueOf(precio);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
        
    }

