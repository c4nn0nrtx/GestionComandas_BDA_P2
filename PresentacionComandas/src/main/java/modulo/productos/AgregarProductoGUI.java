    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        
        // Validación 1: Nombre
        String nombre = txtNombre.getText();
        if (nombre == null || nombre.isEmpty() || nombre.length() <= 2) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese un nombre válido", "Error", JOptionPane.ERROR_MESSAGE);
                txtNombre.requestFocus();
                return;
        }
        
        // Validación 2. TipoProducto seleccionada
        String tipoProductoS = (String) cbxTipoProducto.getSelectedItem();
        if (tipoProductoS == null || tipoProductoS.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione el tipo de producto.", "Error", JOptionPane.ERROR_MESSAGE);
            cbxTipoProducto.requestFocus();
            return;
        }

        // Validación 3: Precio
        if(!validarFormatoPrecio(txtPrecio.getText())){
            JOptionPane.showMessageDialog(this, "Por favor, Ingrese un valor de precio válido.", "Error", JOptionPane.ERROR_MESSAGE);
            txtPrecio.requestFocus();
            return;
        }
        double precio = Double.parseDouble(txtPrecio.getText());
        
        // Validación 4: Tipo de producto
        TipoProducto tipoProducto = null;
        switch (tipoProductoS) {
            case "Platillo" -> tipoProducto = TipoProducto.PLATILLO;
            case "Postre" -> tipoProducto = TipoProducto.POSTRE;
            case "Bebida" -> tipoProducto = TipoProducto.BEBIDA;
            default -> JOptionPane.showMessageDialog(this, "Prror con unidad de medida", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        // Validación 5: Estado
        String estadoSeleccionado = (String) cbxEstadoProducto.getSelectedItem();
        boolean estado;

        if (estadoSeleccionado.equalsIgnoreCase("Habilitar")) {
         estado = true;
        } else if (estadoSeleccionado.equalsIgnoreCase("Deshabilitar")) {
         estado = false;
        } else {
        JOptionPane.showMessageDialog(this, "Seleccione un estado válido.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        try{
            ProductoNuevoDTO productoNuevo = new ProductoNuevoDTO(nombre, tipoProducto, precio, estado);
            productoBO.agregarProducto(productoNuevo);
            JOptionPane.showMessageDialog(this, "Producto creado con éxito", "Error", JOptionPane.INFORMATION_MESSAGE);
        }catch(NegocioException ne){
            JOptionPane.showMessageDialog(this, ne, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

