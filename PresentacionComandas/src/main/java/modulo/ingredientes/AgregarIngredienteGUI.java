    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        
        // Validaci칩n 1: Nombre
        String nombre = txtNombre.getText();
        if (nombre == null || nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, ingrese un nombre", "Error", JOptionPane.ERROR_MESSAGE);
                txtNombre.requestFocus();
                return;
        }
        
        // Validaci칩n 2. UnidadMedida seleccionada
        String unidadMedidaS = (String) cbxUnidadMedida.getSelectedItem();
        if (unidadMedidaS == null || unidadMedidaS.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una unidad de medida.", "Error", JOptionPane.ERROR_MESSAGE);
            cbxUnidadMedida.requestFocus();
            return;
        }

        // Validaci칩n 3: Stock
        double stock;
        try{
            String stockS = (String) txtStock.getText();
            stock = Double.parseDouble(stockS);
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Por favor, Ingrese un valor de stock v치lido.", "Error", JOptionPane.ERROR_MESSAGE);
            txtStock.requestFocus();
            return;
        }
        
        UnidadMedida unidadMedida = null;
        switch (unidadMedidaS) {
            case "Piezas" -> {
                unidadMedida = UnidadMedida.PIEZAS;
            }
            case "Gramos" -> {
                unidadMedida = UnidadMedida.GRAMOS;
            }
            case "Mililitros" -> {
                unidadMedida = UnidadMedida.MILILITROS;
            }
            default -> {
                JOptionPane.showMessageDialog(this, "Prror con unidad de medida", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        try{
            IngredienteNuevoDTO ingredienteNuevo = new IngredienteNuevoDTO(nombre, unidadMedida, stock);
            ingredienteBO.agregarIngrediente(ingredienteNuevo);
            JOptionPane.showMessageDialog(this, "Ingrediente creado con 칠xito", "Error", JOptionPane.INFORMATION_MESSAGE);
        }catch(NegocioException ne){
            JOptionPane.showMessageDialog(this, ne, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

